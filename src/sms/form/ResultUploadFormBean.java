package sms.form;

public class ResultUploadFormBean {

	
	private String [] regNo;
	private String [] studentClass;
	private String [] subjectArr;
	private String [] maxMarks;
	private String [] passingMarks;
	private String [] marksObtained;
	private String [] status;
	
	
	public String[] getRegNo() {
		return regNo;
	}
	public void setRegNo(String [] regNo) {
		this.regNo = regNo;
	}
	public String[] getStudentClass() {
		return studentClass;
	}
	public void setStudentClass(String [] studentClass) {
		this.studentClass = studentClass;
	}
	public String[] getSubjectArr() {
		return subjectArr;
	}
	public void setSubjectArr(String[] subjectArr) {
		this.subjectArr = subjectArr;
	}
	public String[] getMaxMarks() {
		return maxMarks;
	}
	public void setMaxMarks(String[] maxMarks) {
		this.maxMarks = maxMarks;
	}
	public String[] getPassingMarks() {
		return passingMarks;
	}
	public void setPassingMarks(String[] passingMarks) {
		this.passingMarks = passingMarks;
	}
	public String[] getMarksObtained() {
		return marksObtained;
	}
	public void setMarksObtained(String[] marksObtained) {
		this.marksObtained = marksObtained;
	}
	public String[] getStatus() {
		return status;
	}
	public void setStatus(String[] status) {
		this.status = status;
	}
	
}
